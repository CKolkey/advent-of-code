# frozen_string_literal: true

require "debug"

class Graph
  def initialize(input)
    @input = input
    @nodes = {}
  end

  def inspect
    "#<Graph:#{object_id}>"
  end

  def each(&)
    @nodes.values(&)
  end

  def nodes
    @nodes.values
  end

  def build
    @input.each_with_index do |line, y|
      line.each_with_index do |col, x|
        @nodes[[x, y]] = Node.new(col, x, y, self)
      end
    end

    self
  end

  def groups
    @groups ||= begin
      groups  = []
      visited = []

      nodes.each do |node|
        next if visited.include? node

        groups << collect_edges(node)
        visited += groups.last.nodes
      end

      groups
    end
  end

  def find(x, y)
    @nodes[[x, y]] || NullNode.new(x, y, self)
  end

  private

  def collect_edges(node, group = Group.new)
    return if group.include?(node)

    group.add(node)
    node.internal_edges.each { |neighbor| collect_edges(neighbor, group) }
    group
  end
end

class Node
  attr_reader :value

  def initialize(value, x, y, graph)
    @value = value
    @x     = x
    @y     = y
    @graph = graph
  end

  def position
    [@x, @y]
  end

  def ==(other)
    position == other&.position
  end

  def external_edges
    edges.select { _1.value.nil? || _1.value != value }
  end

  def internal_edges
    edges.compact.select { _1.value == value }
  end

  def edges
    [
      @graph.find(@x - 1, @y),
      @graph.find(@x + 1, @y),
      @graph.find(@x, @y - 1),
      @graph.find(@x, @y + 1)
    ]
  end
end

class NullNode
  attr_reader :value

  def initialize(x, y, graph)
    @value = nil
    @x = x
    @y = y
    @graph = graph
  end

  def position
    [@x, @y]
  end
end

class Group
  attr_reader :nodes

  def initialize
    @nodes = []
  end

  def include?(node)
    @nodes.include? node
  end

  def empty?
    @nodes.empty?
  end

  def add(node)
    @nodes << node
  end

  def area
    @nodes.size
  end

  def perimeter
    bordering = []
    @nodes.each do |node|
      node.external_edges.each do |neighbor|
        bordering << neighbor
      end
    end

    bordering.size
  end

  def score
    area * perimeter
  end
end

