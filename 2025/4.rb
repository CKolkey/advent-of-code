def build_map(input)
  Set.new.tap do |map|
    input.split.map(&:chars).each_with_index do |row, y|
      row.each_with_index do |position, x|
        next unless position == "@"

        map << [x, y]
      end
    end
  end
end

def removable_points(map)
  map.select { |loc| count_adjacent(map, *loc) < 4 }
end

def count_adjacent(locations, x, y)
  [-1, 0, 1].repeated_permutation(2).count do |dx, dy|
    next if dx.zero? && dy.zero?

    locations.include?([x + dx, y + dy])
  end
end

def solve(input, bound)
  count = 0
  state = build_map(input)
  (1..bound).each do
    points = removable_points(state)
    break if points.empty?

    count += points.count
    state = state.subtract(points)
  end

  count
end

